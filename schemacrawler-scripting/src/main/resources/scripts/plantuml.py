# Tables
for schema in catalog.getSchemas():
    if not catalog.getTables(schema):
        continue
    print(f'$schema("{support.cleanFullName(schema)}", "{schema.key().slug()}") {{')
    print()
    for table in catalog.getTables(schema):
        print(f'${support.type(table)}("{support.cleanName(table)}", "{table.key().slug()}") {{')
        for column in table.getColumns():
            if column.isPartOfPrimaryKey():
                col_macro = '  $pk'
            elif column.isPartOfForeignKey():
                col_macro = '  $fk'
            else:
                col_macro = '  $column'
            not_null = 'NOT NULL' if not column.isNullable() else ''
            print(f'{col_macro}("{column.getName()}"): {support.columnType(column)} {not_null}')
        print('}')
        print()
        if table.hasRemarks():
            print(f'note left of {table.key().slug()} #LemonChiffon')
            print(table.getRemarks())
            print('end note')
            print()
        for column in table.getColumns():
            if column.hasRemarks():
                print(f'note right of {table.key().slug()}::{column.getName()} #LightCyan')
                print(column.getRemarks())
                print('end note')
                print()
        print()
        print()
    print('}')
    print()
    print()

# Foreign keys
for table in catalog.getTables():
    for fk in table.getExportedForeignKeys():
        pk_table = fk.getPrimaryKeyTable()
        fk_table = fk.getForeignKeyTable()
        pk_schema_slug = pk_table.getSchema().key().slug()
        pk_table_slug = pk_table.key().slug()
        fk_schema_slug = fk_table.getSchema().key().slug()        
        fk_table_slug = fk_table.key().slug()
        for columnReference in fk.getColumnReferences():
            pk_col_name = support.cleanName(columnReference.getPrimaryKeyColumn())
            fk_col_name = support.cleanName(columnReference.getForeignKeyColumn())
            print(f'{pk_schema_slug}.{pk_table_slug}::{pk_col_name} ||--o{{ {fk_schema_slug}.{fk_table_slug}::{fk_col_name}', end='')
            if support.hasName(fk):
                print(f' : {fk.getName()}', end='')
            print()
print()

