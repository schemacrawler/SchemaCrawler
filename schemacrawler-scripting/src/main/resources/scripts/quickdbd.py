def symbol_for(cardinality):
  """Map `RelationshipCardinality` enum to Mermaid notation."""
  card_map = {
    'unknown':   '-',  # Default to 1:1 or adjust
    'zero_one':  '0-',
    'zero_many':  '>0-',
    'one_one':   '-',
    'one_many':  '>-',
    'many_many':  '>-<' # (bridge table implied)
  }
  return card_map.get(cardinality.name(), '||--o{')


